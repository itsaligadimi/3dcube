package com.agadimi.a3dcube;

public class Square
{
    private Edge top, left, bottom, right;

    public Edge getTop()
    {
        return top;
    }

    public void setTop(Edge top)
    {
        this.top = top;
    }

    public Edge getLeft()
    {
        return left;
    }

    public void setLeft(Edge left)
    {
        this.left = left;
    }

    public Edge getBottom()
    {
        return bottom;
    }

    public void setBottom(Edge bottom)
    {
        this.bottom = bottom;
    }

    public Edge getRight()
    {
        return right;
    }

    public void setRight(Edge right)
    {
        this.right = right;
    }
}
